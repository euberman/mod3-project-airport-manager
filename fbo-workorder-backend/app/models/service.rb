class Service < ApplicationRecord



  # Jet Fuel -Pilot provides fuel order to Customer service desk and then CSR radio’s line personnel the order.

  # Towing service- Pilot or owner call to have plane moved from hangar to ramp area. (or moved from ramp to hangar).

  # Overnight hangar- some aircraft request being in hanger overnight.

  # GPU’s service (portable generators we hook up to planes so they don’t run down their batteries while pilots do checks, etc.)

  # Lav service – cleaning toilet waist from aircraft.

  # Catering orders- pilots or owners order food to be ready when they depart.

  # Water service- adding drinking water to aircraft’s water source.

  # Crew Cars- Cars available on-site (to go shopping or get food), usually 4 hours or less.

 
end
